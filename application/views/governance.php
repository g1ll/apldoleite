                        <p>Atualmente, sua governança é composta por 14 instituições, entre públicas e privadas, que oferecem apoio em áreas como ensino, capacitação, pesquisa, extensão, tecnologia, assistência técnica, fomento, e representação dos próprios produtores e empresas. Estão ligados diretamente ao APL cerca de 1000 produtores de leite. Estes, consideradas como empreendedores, somam, junto com suas famílias, pelo menos, cerca de 3000 pessoas que tem sua reprodução socioeconômica baseada diretamente na produção leiteira, sendo responsáveis pela produção de mais de 60 mil litros de leite por dia, além de produtos derivados.</p>           
