                        <p>Embora o APL do Leite tenha origem em Santana do Livramento e referencie essa origem no nome, não está restrito a este município. Também compõem o Arranjo outros três municípios da Fronteira Oeste, através de produtores de leite de Rosário do Sul, São Gabriel e Santa Margarida do Sul. Aliás, é consenso a importância de que, dentro do possível, seja expandida a atuação do APL em âmbito regional, dado o crescimento que a cadeia produtiva tem experimentado e o papel socioeconômico que vem alcançando.
                            Na Fronteira Oeste, a participação do campo ainda se restringe basicamente a pecuária de corte extensiva, a produção de arroz e mais recentemente a produção de soja, apesar das inúmeras potencialidades já evidenciadas e das iniciativas inovadoras postas em prática, sendo reconhecida a necessidade de novos elementos de dinamização da economia regional, agregação de valor e geração de trabalho e renda. Com o histórico predomínio das médias e grandes propriedades voltadas à pecuária de corte, a produção de leite esteve presente na Fronteira Oeste, sobretudo, para consumo interno nas propriedades, com baixo volume de excedente para comercialização.</p>
                        <p>No entanto, a partir dos anos 1990, com a instalação de diversos assentamentos, a região passou a ter considerável incremento, em número e área, de unidades familiares de produção. Apenas nos quatro municípios de abrangência do APL, são mais de 1000 novas unidades, constituindo uma “sementeira de agricultores familiares”. Somente em Santana do Livramento são 33 assentamentos, a maior concentração do Rio Grande do Sul. Tão logo as famílias assentadas foram experimentando as primeiras estratégias de reprodução socioeconômica nos lotes, foram remetidas à pecuária leiteira, sendo apontada como importante alternativa em pequenas propriedades no caso pampiano, desde que com manejo adequado. Formou-se, então, uma base produtiva com possibilidade de desenvolvimento de uma cadeia do leite significativa na região, oriunda da soma dos produtores mais antigos com os mais recentes, que elencaram a produção leiteira como “carro chefe” de sua reprodução socioeconômica.</p>
