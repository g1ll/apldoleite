                        <p>
                            Fundado em 2009, o Arranjo Produtivo Local do Leite de Santana do Livramento possui uma caminhada de 6 anos de atuação. Desde 2010 é reconhecido pela Agência Gaúcha de Desenvolvimento e Promoção do Investimento (AGDI). Nesta trajetória, o APL do Leite foi, de forma cada vez mais clara, se tornando propriamente um Arranjo Produtivo. Afirma-se isso, sobretudo, pela existência de uma coesão, expressa não só na capacidade de manter os atores articulados diante das dificuldades e limitações, mas, sobretudo, na habilidade de conseguir gerar externalidades positivas mesmo diante dos entraves.
